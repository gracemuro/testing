public class Student
{
    private String name;
    private int absenceCount;
    public Student(String inName, int inAbs)
    {
        name = inName;
        absenceCount = inAbs;
        
    }
        
    public String getName()
    {
        return name;
    }
    
    public int getAbsenceCount()
    {
        return absenceCount;
    }
    
    
        
        
        
    }
